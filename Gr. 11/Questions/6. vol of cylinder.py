#WAP to enter radius and height of cylinder and calculate volume of cylinder.
print('\n\n')
r = float(input('Enter Radius:'))
h = float(input('Enter Height:'))
pi = 3.141592653589793238
vol = (pi*r**2*h)

print('Radius:',r)
print('Height7:',h)
print('\n')
print('The Volume of the Cylinder is',vol,'\b.')
